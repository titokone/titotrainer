public class TitokoneExecutionException extends Exception {

    public TitokoneExecutionException() {
        super();
    }

    public TitokoneExecutionException(String message, Throwable cause) {
        super(message, cause);
    }

    public TitokoneExecutionException(String message) {
        super(message);
    }

    public TitokoneExecutionException(Throwable cause) {
        super(cause);
    }
    
}
